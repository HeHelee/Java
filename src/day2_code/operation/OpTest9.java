package day2_code.operation;

public class OpTest9 {

	public static void main(String[] args) {
		int a = 10;
		
		a += 2; // a = a + 2; //=+ 부호비트 연산이 됨.
		System.out.println(a);
		a -= 2;
		System.out.println(a);
		a *= 2;
		System.out.println(a);
		a /= 2;
		System.out.println(a);
		a %= 2;
		System.out.println(a);
		
	}
}
